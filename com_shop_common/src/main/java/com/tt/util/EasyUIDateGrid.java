package com.tt.util;

import java.util.List;

public class EasyUIDateGrid {

    public  Long total;
    //List<?>表示可以加任何了类型
    public List<?> rows;

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public List<?> getRows() {
        return rows;
    }

    public void setRows(List<?> rows) {
        this.rows = rows;
    }
}
